    public boolean verifiedData() {
        // verifie if first name, last name, password are empty
        if (jTextFieldFirstName.getText().equals("") && jTextFieldLastName.getText().equals("")
                || jTextFieldPhone.getText().equals("") || jTextFieldEmail.getText().equals("")
                || jTextAreaAddress.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Un ou plusieurs champs sont vide");
            return false;
        } // if no image are selected
        else if (imagePath == null) {
            JOptionPane.showMessageDialog(null, "Aucune image selectionnee");
            return false;
        } // if everything are ok
        else {
            return true;
        }

    }

