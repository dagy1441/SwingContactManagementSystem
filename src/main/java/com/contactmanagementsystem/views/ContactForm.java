    public void populateJTable() {

        contactService = new ContactService();

//        List<Contact> contacts = contactService.contactList(currentUserId);
        List<Contact> contacts = contactService.findContactByUserId(currentUserId);
        /**
         * `id`, `first_name`, `last_name`, `contact_group`, `phone`, `email`,
         * `address`, `picture`, `user_id`
         *
         * initialiser les colonnes
         */
        String[] colNames = {"Id", "First Name", "Last Name",
            "Phone", "Email", "Group", "Address", "Picture"};
        //initialiser les ligne
        Object[][] rows = new Object[contacts.size()][8];

        for (int i = 0; i < contacts.size(); i++) {
            rows[i][0] = contacts.get(i).getId();
            rows[i][1] = contacts.get(i).getFirstName();
            rows[i][2] = contacts.get(i).getLastName();
            rows[i][3] = contacts.get(i).getContactGroup();
            rows[i][4] = contacts.get(i).getPhone();
            rows[i][5] = contacts.get(i).getEmail();
            rows[i][6] = contacts.get(i).getAddress();
            ImageIcon pic = new ImageIcon(new ImageIcon(contacts.get(i).getPicture())
                    .getImage().getScaledInstance(80, 50, Image.SCALE_SMOOTH));
            rows[i][7] = pic;
            //rows[i][8] = "";
        }

        //
        TableModel contactTable = new TableModel(rows, colNames);
        jTableContact.setModel(contactTable);
        jTableContact.setRowHeight(50);
        jTableContact.getColumnModel().getColumn(7).setPreferredWidth(80);

    }

    public void refreshJTable() {
        jTableContact.setModel(new DefaultTableModel());
        populateJTable();
    }

