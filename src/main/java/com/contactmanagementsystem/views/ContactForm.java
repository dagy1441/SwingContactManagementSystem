    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        if (!jTextFieldId.getText().equals("")) {
            long id = Long.valueOf(jTextFieldId.getText());
            String firstName = jTextFieldFirstName.getText();
            String lastName = jTextFieldLastName.getText();
            String phone = jTextFieldPhone.getText();
            String email = jTextFieldEmail.getText();
            String address = jTextAreaAddress.getText();
            String group = jComboBoxGroup.getSelectedItem().toString();

            // if thne user want to update the data and the image
            if (imagePath != null) {

                byte[] img = null;

                try {
                    Path path = Paths.get(imagePath);

                    img = Files.readAllBytes(path);
                    //InputStream image = new FileInputStream(new File(imagePath));
                    Contact contact = new Contact(id, firstName, lastName, phone, email, group, address, img, currentUserId);

                    contactService.updateContact(contact, true);

                    refreshJTable();

                    clearContactFiels();

                } catch (IOException ex) {
                    Logger.getLogger(ContactForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                Contact contact = new Contact(id, firstName, lastName, phone, email, group, address, null, currentUserId);

                contactService.updateContact(contact, false);

                refreshJTable();

                clearContactFiels();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Select a contact from table");
        }


    }//GEN-LAST:event_jButtonUpdateActionPerformed

